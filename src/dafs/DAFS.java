    private void btnAttendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAttendActionPerformed
        javax.swing.JPanel panel = new javax.swing.JPanel();
        javax.swing.JLabel label = new javax.swing.JLabel("Enter your pin");
        javax.swing.JPasswordField pass = new javax.swing.JPasswordField(4);
        
        panel.add(label);
        panel.add(pass);
        
        String[] options = new String[]{"OK", "Cancel"};
        int option = JOptionPane.showOptionDialog(null, panel, "Verification", JOptionPane.NO_OPTION, JOptionPane.PLAIN_MESSAGE, null, options, options[0]);
 
        if(option==0)
        {
            char[] password = pass.getPassword();
            
            if(new String(password).equals(s.password))
            {
                new AttendanceFrame().setVisible(true);
                dispose();
            }
            else
                JOptionPane.showMessageDialog(null, "Invalid Pin");
        }
    }//GEN-LAST:event_btnAttendActionPerformed

