            public void run() {
                DAFS dafs = new DAFS();
                dafs.setVisible(true);
                if(isRegisterFlag==false)
                        dafs.dispose();

                Calendar cal = Calendar.getInstance();
                int currentHour = cal.get(Calendar.HOUR);
                int currentMinute = cal.get(Calendar.MINUTE);
                
          
                if(currentHour==10&&currentMinute==13)
                {
                    new Notification().setVisible(true);
                    
                    Date temp = new Date();
                     
                    for (int i = 0; i < dafs.s.eventCount; i++) 
                        if (dafs.s.event[i].date.getDate() == temp.getDate()
                                && dafs.s.event[i].date.getMonth() == temp.getMonth()
                                && dafs.s.event[i].date.getYear() == temp.getYear()) {
                            JOptionPane.showMessageDialog(null, dafs.s.event[i].name);                                                       
                        }
                        
                if(dafs.s.budget.budgetRemaining<dafs.s.budget.crucialPoint)
                        JOptionPane.showMessageDialog(null, "Warning, you have very less money left");
                
         
                }
               
            }
